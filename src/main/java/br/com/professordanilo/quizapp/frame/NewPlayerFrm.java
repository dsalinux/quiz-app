    private void updateComponentValues(){
        txtName.setText(player.getName());
        switch (player.getPlayerType()) {
            case GROUP:
                tbGrupeType.setSelected(true);
                break;
            case MALE:
                tbMaleType.setSelected(true);
                break;
            case FEMALE:
                tbFemaleType.setSelected(true);
                break;
        }
    }

