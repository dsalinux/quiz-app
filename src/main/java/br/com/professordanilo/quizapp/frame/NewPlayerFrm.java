    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        player.setName(txtName.getText().trim());
        if(StringHelper.isEmpty(player.getName())){
            SwingUtil.addMessageWarn("Informe o nome do competidor.");
            return;
        }
        if(groupPlayerType.getSelection() == null){
            SwingUtil.addMessageWarn("Selecione o tipo de competidor.");
            return;
        }
        switch (groupPlayerType.getSelection().getActionCommand()) {
            case "group":
                player.setPlayerType(Player.PlayerType.GROUP);
                break;
            case "male":
                player.setPlayerType(Player.PlayerType.MALE);
                break;
            case "female":
                player.setPlayerType(Player.PlayerType.FEMALE);
                break;
            default:
                throw new AssertionError();
        }
        dispose();
        
    }//GEN-LAST:event_btnAddActionPerformed

